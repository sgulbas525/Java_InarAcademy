public class Checkpoint03_37_39 {
	public static void main(String[] args){

		System.out.println("true || true && false ---> " + (true || true && false));
		System.out.println("true && true || false ---> " + (true && true || false));

		System.out.println("2 * 2 - 3 > 2 && 4 – 2 > 5 ---> " + ((2 * 2 - 3 > 2) && (4 - 2 > 5)));
		System.out.println("2 * 2 - 3 > 2 || 4 – 2 > 5 ---> " + ((2 * 2 - 3 > 2) || (4 - 2 > 5)));
		}
}
